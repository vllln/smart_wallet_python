# 1. ФУНКЦИЯ ДЛЯ ОТЧЕТА
# Мы создаем ее заранее, чтобы вызвать в самом конце
def print_report(spent_history, current_balance):
    print("\n" + "="*30)
    print("      ИТОГОВЫЙ ОТЧЕТ      ")
    print("="*30)
    
    # Перебираем каждую покупку в списке истории
    for p in spent_history:
        # Достаем название и цену по ключам словаря
        print(f"• {p['название']}: {p['цена']} руб.")
    
    print("-" * 30)
    print(f"Остаток в кошельке: {current_balance} руб.")
    print("="*30)

# --- ОСНОВНАЯ ЛОГИКА ПРОГРАММЫ ---

print("Добро пожаловать в Smart Wallet v1.0")

# Получаем стартовый капитал
balance = float(input("Введите ваш начальный баланс: "))
history = [] # Список, где будут лежать наши словари-покупки

# Цикл работает, пока на счету есть деньги
while balance > 0:
    item = input("\nНа что тратим? (или напиши 'стоп' для выхода): ")
    
    # Проверяем, не хочет ли пользователь выйти раньше
    if item.lower() == 'стоп':
        break
        
    price = float(input(f"Сколько стоит '{item}'? "))

    # Проверка: хватит ли денег на эту покупку?
    if price > balance:
        print(f"Недостаточно средств! (Доступно лишь {balance} руб.)")
        continue # Пропускаем запись и возвращаемся в начало цикла

    # Если денег хватает:
    balance -= price # Уменьшаем баланс (balance = balance - price)
    
    # Создаем запись в виде словаря и добавляем в список истории
    purchase = {"название": item, "цена": price}
    history.append(purchase)
    
    print(f"Успешно! Остаток: {balance} руб.")

# 2. ВЫВОД РЕЗУЛЬТАТОВ
# Если в списке истории есть хотя бы одна запись — печатаем отчет
if len(history) > 0:
    print_report(history, balance)
else:
    print("\nТрат не было. Баланс сохранен!")

# Твоя коронная проверка в конце
if balance <= 0:
    print("\nхунйя, деньги кончились!")
